<p>NOTES
<p>
<ol>
  <li><a name="note1">Changes of use within a specific</a> class do not require planning permission <i>provided that:</i> the use subsists; the planning permission has been implemented; and no restrictive condition is attached to the consent
  <li><a name="note2">The permitted development rights shown</a> in the final column <i> do not</i> require planning permission <i>where all criteria</i> set out in the Order are met and <i> no restrictive conditions apply</i>
  <li><a name="note3">The General Development</a> (Amendment) Order 1995 provides for the permitted change of use of a building from Class A1 or Class A2 to a mixed use for the purposes of either Class A1 or Class A2 and as a single flat, and from such a mixed use to Class A1 or Class A2 use <i>provided</i> the Class A1 or Class A2 use is situated on a floor below the single flat and various other conditions are met.
  <li><a name="note4"><i>Sui Generis</i></a> is a term which refers to a use on its own. Any planning use not falling within a specific class within the Classes Order falls within this category.
  <li><a name="note5">A B1 use must be capable of being</a> undertaken &quot;in any residential area without detriment to the amenity of that area by reason of noise, vibration, smell, fumes, smoke, soot, ash, dust or grit&quot;.
  <li><a name="note6">The Use Classes (Amendment) Order</a> 1995 amends the 1987 Order by omitting Classes B4 - B7 (special industrial groups B - E). Industrial processes previously within these classes now fall within Class B2 (general industrial).
  <li><a name="note7">A C1 use is defines as a hotel or a </a>boarding or guest house where, in each case, no significant element of care is provided.
  <li><a name="note8">Six or less residents unless living</a> together as a family.
  <li><a name="note9">It should be noted that permitted</a> changes of use identified above are ratchet changes, i.e. cannot be made in reverse.
  <li><a name="note10">Various other permitted development</a> rights exist for, <i>inter alia</i>, industrial and warehouse buildings, dwelling houses, agricultural buildings, schools, colleges, universities, hospitals and development by statutory undertakers.
  <li>The above is intended as a general guide only. Reference must be made in the final instance to the Town and Country Planning Act 1990 as amended, the Use Classes Order 1987 as amended, and the General Development Order 1988 as amended.
</ol>
